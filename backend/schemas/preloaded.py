class PreloadedBenchmarkPack(BaseModel):
    """Top-level schema for a pre-loaded benchmark pack (.auditforge.json).

    This is the single source of truth that ships with AuditForge for each
    supported benchmark. It contains everything needed to scan, evaluate,
    detect false positives, and produce intelligent reports — without
    requiring any LLM at audit time.
    """

    schema_version: str = Field(
        default=CURRENT_SCHEMA_VERSION,
        description="Pack schema version for forward compatibility",
    )
    benchmark: PreloadedBenchmarkMeta = Field(
        ..., description="Benchmark identification and metadata"
    )
    report_profile: ReportProfile = Field(
        ..., description="Report configuration: narrative groups, risk weights, chart preferences"
    )
    rules: list[PreloadedRule] = Field(
        ..., min_length=1, description="Array of enriched rules"
    )

    # ── Cross-field validators ───────────────────────────────────────────

    @model_validator(mode="after")
    def validate_rule_count_matches(self) -> PreloadedBenchmarkPack:
        """Ensure benchmark.total_rules matches actual rules count."""
        actual = len(self.rules)
        declared = self.benchmark.total_rules
        if actual != declared:
            raise ValueError(
                f"benchmark.total_rules is {declared} but rules array has {actual} entries"
            )
        return self

    @model_validator(mode="after")
    def validate_narrative_group_references(self) -> PreloadedBenchmarkPack:
        """Ensure every rule's narrative_group references a defined group."""
        defined_groups = set(self.report_profile.narrative_groups.keys())
        orphans: list[str] = []
        for rule in self.rules:
            if rule.narrative_group and rule.narrative_group not in defined_groups:
                orphans.append(f"{rule.section_number} -> '{rule.narrative_group}'")
        if orphans:
            raise ValueError(
                f"Rules reference undefined narrative_groups: {', '.join(orphans[:10])}"
                + (f" (+{len(orphans) - 10} more)" if len(orphans) > 10 else "")
            )
        return self

    @model_validator(mode="after")
    def validate_no_duplicate_sections(self) -> PreloadedBenchmarkPack:
        """Ensure no duplicate section_number values in the rules array."""
        seen: dict[str, int] = {}
        dupes: list[str] = []
        for i, rule in enumerate(self.rules):
            if rule.section_number in seen:
                dupes.append(rule.section_number)
            seen[rule.section_number] = i
        if dupes:
            raise ValueError(
                f"Duplicate section_number values: {', '.join(dupes[:10])}"
            )
        return self

    @model_validator(mode="after")
    def validate_group_with_references(self) -> PreloadedBenchmarkPack:
        """Ensure group_with references point to rules that exist in the pack."""
        all_sections = {r.section_number for r in self.rules}
        bad_refs: list[str] = []
        for rule in self.rules:
            for ref in rule.group_with:
                if ref not in all_sections:
                    bad_refs.append(f"{rule.section_number}.group_with -> '{ref}'")
        if bad_refs:
            raise ValueError(
                f"group_with references non-existent rules: {', '.join(bad_refs[:10])}"
                + (f" (+{len(bad_refs) - 10} more)" if len(bad_refs) > 10 else "")
            )
        return self


