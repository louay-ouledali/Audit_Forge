class PackValidationResult(BaseModel):
    """Summary of a pack validation run."""

    valid: bool = Field(..., description="Whether the pack passed all checks")
    errors: list[str] = Field(default_factory=list, description="Validation errors")
    warnings: list[str] = Field(default_factory=list, description="Non-fatal warnings")
    stats: dict[str, Any] = Field(default_factory=dict, description="Completeness statistics")


def validate_pack(path: str | Path) -> tuple[PreloadedBenchmarkPack | None, PackValidationResult]:
    """Load and validate a .auditforge.json pack file.

    Parameters
    ----------
    path : str | Path
        Path to the .auditforge.json file.

    Returns
    -------
    tuple[PreloadedBenchmarkPack | None, PackValidationResult]
        The parsed pack (or None if validation failed) and a detailed result.
    """
    path = Path(path)
    errors: list[str] = []
    warnings: list[str] = []

    # ── File-level checks ────────────────────────────────────────────────
    if not path.exists():
        return None, PackValidationResult(valid=False, errors=[f"File not found: {path}"])
    if not path.suffix == ".json" and not path.name.endswith(".auditforge.json"):
        warnings.append(f"Unexpected file extension: {path.suffix} (expected .auditforge.json)")

    try:
        raw = path.read_text(encoding="utf-8")
    except Exception as exc:
        return None, PackValidationResult(valid=False, errors=[f"Cannot read file: {exc}"])

    try:
        data = json.loads(raw)
    except json.JSONDecodeError as exc:
        return None, PackValidationResult(valid=False, errors=[f"Invalid JSON: {exc}"])

    # ── Schema validation ────────────────────────────────────────────────
    try:
        pack = PreloadedBenchmarkPack.model_validate(data)
    except Exception as exc:
        return None, PackValidationResult(valid=False, errors=[str(exc)])

    # ── Completeness checks (warnings, not errors) ───────────────────────
    total = len(pack.rules)
    with_commands = sum(1 for r in pack.rules if r.audit_command)
    with_expression = sum(1 for r in pack.rules if r.expected_output_expression)
    with_empty_interp = sum(1 for r in pack.rules if r.empty_output_interpretation)
    with_fp_conditions = sum(1 for r in pack.rules if r.fp_conditions)
    with_narrative = sum(1 for r in pack.rules if r.narrative_group)
    with_mitre = sum(1 for r in pack.rules if r.mitre_attack)
    with_remediation = sum(1 for r in pack.rules if r.remediation_command)

    pct = lambda n: round(n / total * 100) if total else 0  # noqa: E731

    if with_commands < total:
        warnings.append(f"{total - with_commands} rules missing audit_command ({pct(with_commands)}% complete)")
    if with_expression < total:
        warnings.append(f"{total - with_expression} rules missing expected_output_expression ({pct(with_expression)}% complete)")
    if with_empty_interp < total * 0.9:
        warnings.append(f"Only {pct(with_empty_interp)}% of rules have empty_output_interpretation (target: >=90%)")
    if with_fp_conditions < total * 0.5:
        warnings.append(f"Only {pct(with_fp_conditions)}% of rules have fp_conditions (target: >=80%)")
    if with_narrative < total:
        warnings.append(f"{total - with_narrative} rules missing narrative_group ({pct(with_narrative)}% complete)")
    if with_mitre < total * 0.7:
        warnings.append(f"Only {pct(with_mitre)}% of rules have MITRE ATT&CK mapping (target: >=70%)")

    # ── Related/group_with symmetry check ────────────────────────────────
    section_set = {r.section_number for r in pack.rules}
    for rule in pack.rules:
        for ref in rule.related_rules:
            if ref not in section_set:
                warnings.append(
                    f"Rule {rule.section_number}: related_rules references '{ref}' which is not in the pack"
                )

    stats = {
        "total_rules": total,
        "with_audit_command": with_commands,
        "with_expression": with_expression,
        "with_empty_interpretation": with_empty_interp,
        "with_fp_conditions": with_fp_conditions,
        "with_narrative_group": with_narrative,
        "with_mitre_attack": with_mitre,
        "with_remediation_command": with_remediation,
        "narrative_groups_defined": len(pack.report_profile.narrative_groups),
        "completeness_pct": {
            "commands": pct(with_commands),
            "expressions": pct(with_expression),
            "empty_interpretation": pct(with_empty_interp),
            "fp_conditions": pct(with_fp_conditions),
            "narrative_groups": pct(with_narrative),
            "mitre_attack": pct(with_mitre),
            "remediation_commands": pct(with_remediation),
        },
    }

    return pack, PackValidationResult(
        valid=True,
        errors=errors,
        warnings=warnings,
        stats=stats,
    )


def compute_pack_hash(path: str | Path) -> str:
    """Compute SHA-256 hash of a pack file for change detection."""
    sha256 = hashlib.sha256()
    with open(path, "rb") as f:
        for chunk in iter(lambda: f.read(8192), b""):
            sha256.update(chunk)
    return sha256.hexdigest()
