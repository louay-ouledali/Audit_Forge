def calibrate_scan_results(
    findings: list[Any],
    db: Any = None,
    auto_apply: bool = False,
) -> list[dict[str, Any]]:
    """Review FAIL findings from a scan and suggest expression calibrations.

    Returns a list of calibration suggestions. If *auto_apply* is True
    and the suggestion has high confidence, updates the RuleCommand.
    """
    suggestions: list[dict[str, Any]] = []

    for finding in findings:
        if finding.status != "FAIL":
            continue

        output = finding.actual_output or ""
        expr = finding.expected_output or ""
        rule = finding.rule if hasattr(finding, "rule") else None
        title = rule.title if rule else ""

        calibrated = calibrate_expression(output, expr, title, finding.status)
        if calibrated:
            suggestions.append({
                "finding_id": finding.id,
                "rule_id": finding.rule_id,
                "current_expression": expr,
                "suggested_expression": calibrated,
                "actual_output": output[:200],
                "rule_title": title,
            })

            if auto_apply and db:
                _apply_calibration(finding, calibrated, db)

    logger.info("Calibration: %d suggestions from %d findings",
                len(suggestions), len(findings))
    return suggestions


def _apply_calibration(finding: Any, new_expr: str, db: Any) -> None:
    """Apply a calibration to the RuleCommand behind a finding."""
    try:
        from backend.models.rule_command import RuleCommand
        from datetime import datetime, timezone

        cmd = db.query(RuleCommand).filter(
            RuleCommand.rule_id == finding.rule_id
        ).first()
        if cmd and not cmd.is_protected:
            cmd.expected_output_regex = new_expr
            cmd.confidence_score = 0.70
            cmd.confidence_source = "calibrated"
            cmd.updated_at = datetime.now(timezone.utc)
            db.commit()
    except Exception as exc:
        logger.warning("Failed to apply calibration: %s", exc)
        try:
            db.rollback()
        except Exception:
            pass
